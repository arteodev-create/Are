const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const saved = localStorage.getItem('veritas-language');
    if (saved && languages[saved]) return saved;
    const browserLanguage = navigator.language?.toLowerCase() || '';
    if (browserLanguage.startsWith('ko')) return 'ko';
    return browserLanguage.startsWith('vi') ? 'vi' : 'en';
  });

  function setLanguage(nextLanguage) {
    const safeLanguage = languages[nextLanguage] ? nextLanguage : 'vi';
    localStorage.setItem('veritas-language', safeLanguage);
    document.documentElement.lang = safeLanguage;
    setLanguageState(safeLanguage);
  }

  const value = useMemo(() => {
    function t(key, params = {}) {
      const template = dictionary[language]?.[key] ?? dictionary.vi[key] ?? key;
      return Object.entries(params).reduce(
        (text, [param, replacement]) => text.replaceAll(`{${param}}`, String(replacement)),
        template,
      );
    }
    return { language, languages, setLanguage, t };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}
